#include <iostream>

int main() {
	int a = 10;
	float b = 3.14;
	char c = 'A';
	bool  d = true;
	
	std::cout << "Zmienna całkowita: " << a << std::endl;
	std::cout << "Zmienna zmiennoprzecinkowa: " << b << std::endl;
	std::cout << "Zmienna znakowa: " << c << std::endl;
	std::cout << "Zmienna logiczna: " << d << std::endl;
}