#include <iostream>

int main() {
	int pierwsza_liczba = 5;
	int druga_liczba = 3;
    
    std::cout << "Liczba a: " << pierwsza_liczba << std::endl;
    std::cout << "Liczba b: " << druga_liczba << std::endl;
    
    std::cout << "Suma a + b: " << (pierwsza_liczba + druga_liczba) << std::endl;
    std::cout << "Roznica a - b: " << (pierwsza_liczba - druga_liczba) << std::endl;
    std::cout << "Iloraz a * b: " << (pierwsza_liczba * druga_liczba) << std::endl;
    
    return 0;
}
