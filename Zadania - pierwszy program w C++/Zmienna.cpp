#include <iostream>

int main() {
	int a = 5;
	int b = 3;
    
    std::cout << "Liczba a: " << a << std::endl;
    std::cout << "Liczba b: " << b << std::endl;
    
    std::cout << "Suma a + b: " << (a + b) << std::endl;
    std::cout << "Roznica a - b: " << (a - b) << std::endl;
    std::cout << "Iloraz a * b: " << (a * b) << std::endl;
    
    return 0;
}